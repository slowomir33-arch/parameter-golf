def load_data_shard(file: Path) -> Tensor:
    header_bytes = 256 * np.dtype("<i4").itemsize
    token_bytes = np.dtype("<u2").itemsize
    header = np.fromfile(file, dtype="<i4", count=256)
    if header.size != 256 or int(header[0]) != 20240520 or int(header[1]) != 1:
        raise ValueError(f"Unexpected shard header for {file}")
    num_tokens = int(header[2])
    expected_size = header_bytes + num_tokens * token_bytes
    if file.stat().st_size != expected_size:
        raise ValueError(f"Shard size mismatch for {file}")
    tokens_np = np.fromfile(file, dtype="<u2", count=num_tokens, offset=header_bytes)
    if tokens_np.size != num_tokens:
        raise ValueError(f"Short read for {file}")
    return torch.from_numpy(tokens_np.astype(np.uint16, copy=False))


class TokenStream:
    def __init__(self, pattern: str):
        self.files = [Path(p) for p in sorted(glob.glob(pattern))]
        if not self.files:
            raise FileNotFoundError(f"No files found for pattern: {pattern}")
        self.file_idx = 0
        self.tokens = load_data_shard(self.files[0])
        self.pos = 0

    def _advance_file(self) -> None:
        self.file_idx = (self.file_idx + 1) % len(self.files)
        self.tokens = load_data_shard(self.files[self.file_idx])
        self.pos = 0

    def take(self, n: int) -> Tensor:
        chunks: list[Tensor] = []
        remaining = n
        while remaining > 0:
            avail = self.tokens.numel() - self.pos
            if avail <= 0:
                self._advance_file()
                continue
            k = min(remaining, avail)
            chunks.append(self.tokens[self.pos : self.pos + k])
            self.pos += k
            remaining -= k
        return chunks[0] if len(chunks) == 1 else torch.cat(chunks)


class DistributedTokenLoader:
    def __init__(self, pattern: str, rank: int, world_size: int, device: torch.device):
        self.rank, self.world_size, self.device = rank, world_size, device
        self.stream = TokenStream(pattern)

    def next_batch(self, global_tokens: int, seq_len: int, grad_accum_steps: int) -> tuple[Tensor, Tensor]:
        local_tokens = global_tokens // (self.world_size * grad_accum_steps)
        per_rank_span = local_tokens + 1
        chunk = self.stream.take(per_rank_span * self.world_size)
        start = self.rank * per_rank_span
        local = chunk[start : start + per_rank_span].to(dtype=torch.int64)
        x = local[:-1].reshape(-1, seq_len)
        y = local[1:].reshape(-1, seq_len)
        return x.to(self.device, non_blocking=True), y.to(self.device, non_blocking=True)


# ============================================================
# LOGOS-44 MODEL ARCHITECTURE
# ============================================================
# Key idea: Instead of stacking N separate transformer layers,
# we route the signal 44 times through a single shared block
# (toroidal bottleneck + CDMA field + coherence gate).
# This trades parameters for compute depth.

class RMSNorm(nn.Module):
    def __init__(self, eps: float | None = None):
        super().__init__()
        self.eps = eps

    def forward(self, x: Tensor) -> Tensor:
        return F.rms_norm(x, (x.size(-1),), eps=self.eps)


class CastedLinear(nn.Linear):
    def forward(self, x: Tensor) -> Tensor:
        bias = self.bias.to(x.dtype) if self.bias is not None else None
        return F.linear(x, self.weight.to(x.dtype), bias)


class PhaseEncoding(nn.Module):
    """Learned sinusoidal positional encoding."""
    def __init__(self, dim: int, max_seq: int = 1024):
        super().__init__()
        self.frequencies = nn.Parameter(torch.randn(dim) * 0.1)
        self.phase_offset = nn.Parameter(torch.zeros(dim))

    def forward(self, seq_len: int, device: torch.device, dtype: torch.dtype) -> Tensor:
        t = torch.arange(seq_len, device=device, dtype=torch.float32)
        phases = t.unsqueeze(1) * self.frequencies.unsqueeze(0) + self.phase_offset.unsqueeze(0)
        return torch.sin(phases).to(dtype=dtype)


class ToroidalBottleneck(nn.Module):
    """Project to low-dim space, apply sin·cos toroidal geometry."""
    def __init__(self, dim: int, rank: int):
        super().__init__()
        self.rank = rank
        self.project = CastedLinear(dim, rank * 2, bias=False)
        self.angular = nn.Parameter(torch.ones(rank) * 0.5)

    def forward(self, x: Tensor) -> Tensor:
        h = self.project(x)
        theta = h[..., :self.rank] * self.angular.to(x.dtype)
        phi = h[..., self.rank:] * self.angular.to(x.dtype)
        return torch.sin(theta) * torch.cos(phi)


class CDMAField(nn.Module):
    """128-signal field decoded via learned spreading codes."""
    def __init__(self, n_signals: int, dim: int, code_len: int):
        super().__init__()
        self.n_signals = n_signals
        self.code_len = code_len
        self.signals = nn.Parameter(torch.randn(n_signals, dim) * 0.02)
        codes = torch.randn(n_signals, code_len)
        codes = codes / (codes.norm(dim=-1, keepdim=True) + 1e-8)
        self.register_buffer('codes', codes.contiguous())
        self.key_to_code = CastedLinear(code_len, code_len, bias=False)
        self.signal_norm = nn.LayerNorm(dim)

    def decode(self, key: Tensor) -> Tensor:
        code = self.key_to_code(key)
        code = code / (code.norm(dim=-1, keepdim=True) + 1e-8)
        correlation = torch.einsum('...c, nc -> ...n', code.to(self.codes.dtype), self.codes)
        weights = torch.softmax(correlation * 4.0, dim=-1)
        field = self.signal_norm(self.signals)
        return torch.einsum('...n, nd -> ...d', weights.to(field.dtype), field)


class CoherenceGate(nn.Module):
    """Learned sigmoid blend between field signal and residual."""
    def __init__(self, dim: int):
        super().__init__()
        self.gate_proj = CastedLinear(dim * 2, dim, bias=True)

    def forward(self, field_signal: Tensor, residual: Tensor) -> Tensor:
        gate = torch.sigmoid(self.gate_proj(torch.cat([field_signal, residual], dim=-1)))
        return gate * field_signal + (1 - gate) * residual


class Logos44Field(nn.Module):
    """
    LOGOS-44: Iterative routing through shared toroidal+CDMA block.
    44 passes × 1 shared block = deep compute, shallow parameter count.
    """
    def __init__(
        self,
        vocab_size: int = 1024,
        dim: int = 512,
        rank: int = 64,
        depth: int = 44,
        n_signals: int = 128,
        max_seq: int = 1024,
        tie_embeddings: bool = True,
        tied_embed_init_std: float = 0.005,
        logit_softcap: float = 30.0,
    ):
        super().__init__()
        self.depth = depth
        self.dim = dim
        self.tie_embeddings = tie_embeddings
        self.logit_softcap = logit_softcap

        self.tok_emb = nn.Embedding(vocab_size, dim)
        self.phase_enc = PhaseEncoding(dim, max_seq)
        self.norm = nn.LayerNorm(dim)
        self.bottleneck = ToroidalBottleneck(dim, rank)
        self.field = CDMAField(n_signals, dim, code_len=rank)
        self.up_proj = CastedLinear(rank, dim, bias=False)
        self.gate = CoherenceGate(dim)
        self.out_norm = RMSNorm()
        self.layer_phase = nn.Parameter(torch.linspace(0, 2 * math.pi, depth))

        # Separate lm_head or tied
        if tie_embeddings:
            self.lm_head = None
            nn.init.normal_(self.tok_emb.weight, mean=0.0, std=tied_embed_init_std)
        else:
            self.lm_head = CastedLinear(dim, vocab_size, bias=False)
            nn.init.zeros_(self.lm_head.weight)

        self._init_weights()

    def _init_weights(self) -> None:
        for m in self.modules():
            if isinstance(m, nn.Linear) and hasattr(m, '_zero_init') and m._zero_init:
                nn.init.zeros_(m.weight)
            elif isinstance(m, nn.Linear) and not isinstance(m, type(self.tok_emb)):
                nn.init.xavier_normal_(m.weight, gain=0.5)

    def forward(self, input_ids: Tensor, target_ids: Tensor) -> Tensor:
        B, T = input_ids.shape
        h = self.tok_emb(input_ids)
        h = F.rms_norm(h, (h.size(-1),))
        pe = self.phase_enc(T, input_ids.device, h.dtype).unsqueeze(0)
        h = h + pe

        for i in range(self.depth):
            res = h
            h = self.norm(h)
            altitude = torch.sin(self.layer_phase[i]).item()
            h = h * (1.0 + 0.1 * altitude)
            key = self.bottleneck(h)
            field_signal = self.field.decode(key)
            key_signal = self.up_proj(key)
            h = self.gate(field_signal + key_signal, res)

        h = self.out_norm(h).reshape(-1, h.size(-1))
        targets = target_ids.reshape(-1)

        if self.tie_embeddings:
            logits = F.linear(h, self.tok_emb.weight)
        else:
            logits = self.lm_head(h)

        logits = self.logit_softcap * torch.tanh(logits / self.logit_softcap)
        return F.cross_entropy(logits.float(), targets, reduction="mean")


